using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.AspNetCore.Mvc.Rendering;
using WebApp2.Areas.Identity.Data;
using WebApp2.Data;

namespace WebApp2.Pages.Contacts
{
    public class CreateModel : PageModel
    {
        private readonly WebApp2.Areas.Identity.Data.WebApp2IdentityDbContext _context;

        public CreateModel(WebApp2.Areas.Identity.Data.WebApp2IdentityDbContext context)
        {
            _context = context;
        }

        public IActionResult OnGet()
        {
            return Page();
        }

        [BindProperty]
        public Contact Contact { get; set; }

        public async Task<IActionResult> OnPostAsync()
        {
            if (!ModelState.IsValid)
            {
                return Page();
            }

            _context.Contact.Add(Contact);
            await _context.SaveChangesAsync();

            return RedirectToPage("./Index");
        }
    }
}